# Dictionary with three items 
dict = { 'A': 'peace', 'B': 4, 'C': 'all' }
  
print("Dictionary items:", dict)
  
# Printing all the items of the Dictionary
print(dict.items())
 
# Printing keys of dictionary
print(dict.keys())

# Printing values of dictionary
print(dict.values())